#!/usr/bin/env node
// scripts/gen-wiki-tree.js
//
// Fetches the obsidian repo wiki file tree via `gh api` and writes
// second-brain/shared/wiki-tree.js with XOR-encoded paths.
//
// The key lives ONLY inside quiz/app.js — the encoded file alone is useless.
// Run: node scripts/gen-wiki-tree.js
// Requires: `gh` CLI authenticated as lucasmasuno.

import { execSync } from 'node:child_process';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { join, dirname } from 'node:path';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const OUT  = join(ROOT, 'shared', 'wiki-tree.js');
const KEY  = 'sb2026xw'; // must match _K in quiz/app.js

function encode(s) {
  return Array.from(s)
    .map((c, i) => (c.charCodeAt(0) ^ KEY.charCodeAt(i % KEY.length)).toString(16).padStart(2, '0'))
    .join('');
}

console.log('Fetching obsidian wiki tree via gh CLI…');
const raw = execSync(
  'gh api "repos/lucasmasunoacn/obsidian/git/trees/main?recursive=1" --jq \'[.tree[].path | select(test("^wiki/.*\\\\.md$"; "i"))]\'',
  { encoding: 'utf-8' }
);

const paths = JSON.parse(raw.trim());
console.log(`Found ${paths.length} wiki files.`);

const encoded = paths.map(encode);

const out = `// shared/wiki-tree.js — auto-generated by scripts/gen-wiki-tree.js
// Paths are XOR-encoded; the decode key lives in quiz/app.js only.
// Do not edit manually. Run: node scripts/gen-wiki-tree.js
window.OB_TREE = ${JSON.stringify(encoded, null, 0)};
`;

writeFileSync(OUT, out, 'utf-8');
console.log(`Written: shared/wiki-tree.js (${encoded.length} entries)`);
